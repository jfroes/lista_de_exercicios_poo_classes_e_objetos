public class Calculadora {
// Exercicio 10: Crie Calculadora com métodos estáticos somar, dividir, etc.

    public static Integer somar(Integer a, Integer b){
        return a + b;
    }

    public static Integer subtrair(Integer a, Integer b){
        return a - b;
    }

    public static Integer multiplicar(Integer a, Integer b){
        return a * b;
    }

    public static Integer dividir(Integer a, Integer b){
        return a / b;
    }
}

